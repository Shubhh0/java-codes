package J.src;
import java.util.*;
public class J12 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n=sc.nextInt();
        int sum =0;
        for( int s=1; s<=n; s++){
            sum = sum+s;

        }

        System.out.println(sum);
    }
}
